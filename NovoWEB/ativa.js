function expandir() {
    document.querySelector('.conteudo-js').style.display = 'block';
    document.querySelector('.botao-js').style.height = '200px';
}

function recolher() {
    document.querySelector('.conteudo-js').style.display = 'none';
    document.querySelector('.botao-js').style.height = '50px';
}

